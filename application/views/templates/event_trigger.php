 <script type="text/javascript">
	
	var myApp = angular.module('myApp',[]);
	
	myApp.controller('personCtrl', function($scope) {
		$scope.hello_world = true;
		$scope.toggle = function() {
			$scope.hello_world = !$scope.hello_world;
		}
	});
 
 </script>